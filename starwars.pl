%%% Qual planeta tem mais mamíferos? %%%
mamifero(Personagem, Planeta) :-
    personagem(Personagem, Especie, Planeta),
    especie(Especie, mammal, _),
    Planeta \= unknown.
total_mamiferos_planeta(Planeta, Total) :-
    findall(Personagem, mamifero(Personagem, Planeta), Lista),
    length(Lista, Total).
tabela_mamiferos(Tabela) :-
    findall(Total-Planeta, (planeta(Planeta, _), 
                           total_mamiferos_planeta(Planeta, Total)), Lista),
    sort(Lista, Ordenada),
    reverse(Ordenada, Tabela).
planetas_empatados_mamiferos(Planetas, Total) :-
    tabela_mamiferos(Tabela),
    Tabela = [Total-_|_],
    findall(P, member(Total-P, Tabela), Planetas).

%%% Qual o planeta com a maior diversidade de espécies? %%%
especie_no_planeta(Planeta, Especie) :-
    personagem(_, Especie, Planeta),
    Especie \= unknown,
    Planeta \= unknown.
total_especies_planeta(Planeta, Total) :-
    setof(Especie, especie_no_planeta(Planeta, Especie), Lista),
    length(Lista, Total).
tabela_especies(Tabela) :-
    findall(Total-Planeta, (planeta(Planeta, _),
              total_especies_planeta(Planeta, Total)), Lista),
    sort(Lista, Ordenada),
    reverse(Ordenada, Tabela).
planetas_empatados_especies(Planetas, Total) :-
    tabela_especies(Tabela),
    Tabela = [Total-_|_],
    findall(P, member(Total-P, Tabela), Planetas).

%%% Qual o planeta tem a maior expectativa de vida? %%%
lifespan_especie(Especie, Lifespan) :-
    especie(Especie, _, Lifespan),
    number(Lifespan).
media_lifespan_planeta(Planeta, Media) :-
    findall(Especie, especie_no_planeta(Planeta, Especie), Especies0),
    sort(Especies0, Especies),         
    Especies \= [],
    findall(L, (member(E, Especies), lifespan_especie(E, L)), Ls),
    sum_list(Ls, Soma),
    length(Ls, N),
    Media is Soma / N.
tabela_media_lifespan(Tabela) :-
    findall(Media-Planeta, (planeta(Planeta, _), 
                           media_lifespan_planeta(Planeta, Media)), Lista),
    sort(Lista, Ordenada),
    reverse(Ordenada, Tabela).
planetas_maior_media_lifespan(Planetas, Media) :-
    tabela_media_lifespan([Media-_|_]),
    tabela_media_lifespan(Tabela),
    findall(P, member(Media-P, Tabela), Planetas).